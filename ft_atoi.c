int		ft_atoi(char *str)
{
	int i;
	int nb;
	int sign;

	i = 0;
	nb = 0;
	sign = 1;
	while ((str[i] == ' ') || (str[i] == '\t') || (str[i] == '\n')
			|| (str[i] == '\v') || (str[i] == '\f') || (str[i] == '\r'))
		i++;
	if (str[i] != 0)
		if ((str[i] == '-') || (str[i] == '+'))
		{
			if (str[i] == '-')
				sign = -1;
			i++;
		}
	while (str[i] >= 48 && str[i] <= 57)
	{
		nb = (nb * 10 + (str[i] - 48));
		i++;
	}
	return (nb * sign);
}
